package logical_Program;

public class Example_36_EvenOdd 
{
	
	public static void main(String[] args) 
	{
		
		int num1=12423;
		
	 
		
		if (num1%2==0) 
		{
			System.out.println("Given Num "+num1+" is an Even Number");
		} 
		else 
		{
			System.out.println("Given Num "+num1+" is an Odd Number");
		}
		
	}

}
