public class Example_35_EvenOdd 
{
	
	
	public static void main(String[] args) 
	{

		int num=8765;;
		
		
	
		if (num%2==0) 
		{
			System.out.println("Given Num is Even Number");
		}
		else
		{
			System.out.println("Given Num is Odd Number");
		}
		
		
	}
	
	

}
