package logical_Program;

public class Example_39_EvenOdd 
{
	
	
	public static void main(String[] args) 
	{
		
		int n = 53;
		
		if(n%2==0)
		{
			System.out.println("Given Number "+n+" is an Even Number");
		}
		
		else
		{
			System.out.println("Given Number "+n+" is an Odd Number");
		}
		
	}

}
