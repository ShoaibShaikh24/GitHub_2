package logical_Program;

public class Example_38_EvenOdd 
{
	
	
	public static void main(String[] args) 
	{
		
		int num = 2404;
		
		if (num%2==0) 
		{
			System.out.println("Given Num "+num+" is an Even Number");
		}
		
		else 
		{
			System.out.println("Given Num "+num+" is an Odd Number");
		}
		
	}

}
