package Logical_Program_1;

public class Program_30_EvenOdd 
{
	
	public static void main(String[] args) 
	{
		
		int a=234; 
		
		if (a%2==0) 
		{
			System.out.println("Given Number is Even Number");
		}
		else 
		{
			System.out.println("Given Number is Odd Number");
		}
		
	}

}
